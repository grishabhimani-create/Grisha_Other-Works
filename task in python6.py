# Set Operations

set1 = {"Apple", "Banana", "Mango", "Orange", "Grapes"}
set2 = {"Mango", "Orange", "Pineapple", "Kiwi", "Apple"}

print("Set 1:", set1)
print("Set 2:", set2)

# Union
print("Union:", set1.union(set2))

# Intersection
print("Intersection:", set1.intersection(set2))

# Difference
print("Difference (set1 - set2):", set1.difference(set2))
